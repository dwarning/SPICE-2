      SUBROUTINE ZERO4(ARRAY,LENGTH)
C     8 byte integer used, no real*4 used
      DOUBLE PRECISION ARRAY(LENGTH)
      IF(LENGTH.EQ.0) RETURN
      DO 10 I=1,LENGTH
         ARRAY(I)=0.0
  10  CONTINUE
      RETURN
      END
