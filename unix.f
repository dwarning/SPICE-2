      SUBROUTINE MOVE (A,I,B,J,N)
      CHARACTER*1 A(1),B(1)
C
C THIS ROUTINE MOVES N CHARACTERS FROM CHARACTER ARRAY B  
C TO CHARACTER ARRAY A, BEGINNING WITH THE J*TH AND I*TH  
C CHARACTER POSITIONS, RESPECTIVELY.  
C
      IF (N.EQ.0) RETURN
      DO 10 K=1,N
         A(I+K-1)=B(J+K-1)
   10 CONTINUE
      RETURN
      END
